_TRANSLATIONS: dict[str, dict[str, str]] = {
    "en": _EN,
    "vi": _VI,
}


def normalize_language(language: str | None = None) -> str:
    """Return a supported language code, honoring the env var when unset."""

    raw = language if language is not None else os.environ.get(LANGUAGE_ENV_VAR)
    normalized = str(raw or "").strip().lower().replace("-", "_")
    normalized = normalized.split("_", 1)[0] if normalized else ""
    if not normalized and raw:
        normalized = str(raw).strip().lower()
    return _ALIASES.get(normalized, DEFAULT_LANGUAGE)


def translations_for(language: str | None = None) -> dict[str, str]:
    """Return English strings overlaid with the selected language."""

    selected = normalize_language(language)
    translations = dict(_TRANSLATIONS[DEFAULT_LANGUAGE])
    translations.update(_TRANSLATIONS.get(selected, {}))
    return translations


def translation_catalog() -> dict[str, dict[str, str]]:
    """Return all supported translation tables for static dashboards."""

    return {
        language: translations_for(language)
        for language in SUPPORTED_LANGUAGES
    }


def available_languages() -> list[dict[str, Any]]:
    """Return supported languages with localized display names."""

    catalog = translation_catalog()
    return [
        {"code": code, "label": catalog[code][f"language.{name}"]}
        for code, name in (("en", "english"), ("vi", "vietnamese"))
    ]


def dashboard_i18n_payload(language: str | None = None) -> dict[str, object]:
    selected = normalize_language(language)
    return {
        "language": selected,
        "available_languages": deepcopy(available_languages()),
        "translations": translations_for(selected),
        "translation_catalog": translation_catalog(),
    }
